    /// <summary>
    /// 指し手のサンプルとその各評価値を保存します。
    /// </summary>
    public sealed class SampleMoveData
    {
        /// <summary>
        /// 指し手の文字列を取得します。
        /// </summary>
        public string MoveText
        {
            get;
            private set;
        }

        /// <summary>
        /// 指し手を取得します。
        /// </summary>
        public Move Move
        {
            get;
            private set;
        }

        /// <summary>
        /// その時の評価値を取得します。
        /// </summary>
        public double Value
        {
            get;
            private set;
        }

        /// <summary>
        /// コンストラクタ
        /// </summary>
        public SampleMoveData(string moveText, double value)
        {
            MoveText = moveText;
            Value = value;
            Move = ShogiParser.ParseMove(moveText, true);

            if (Move == null)
            {
                throw new RagnarokException(
                    "指し手の変換に失敗しました。");
            }
        }
    }

